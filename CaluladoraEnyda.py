import numpy
import math


# Sumar
def sumar(valorUno, valorDos):
    return valorUno + valorDos


# Restar
def restar(valorUno, valorDos):
    return valorUno - valorDos


# Multiplicaci√≥n

def multiplicar(valorUno, valorDos):
    return valorUno * valorDos


# Division

def dividir(valorUno, valorDos):
    return valorUno / valorDos


# Raiz

def raiz(valor1):
    return math.sqrt(valor1)


# Exponente

def exponente(base1, exponente):
    return math.pow(base1, exponente)


# Seno

def seno(valor1):
    return numpy.sin(valor1)
